		public getSound(  _sKey:string ,  _fLoadComplete:Function = null,_fLoadError:Function = null ):void {
			var _sPath:string 	 = this.getSoundPath( _sKey , this.m_sLang );
			var _sOutPath:string = this.m_sUrl + this.m_sLang+"/"+_sPath;
			if( this.m_DictionaySound[_sOutPath] && _fLoadComplete != null ){
				_fLoadComplete( this.m_DictionaySound[_sOutPath] );
			}
			else {
				var _liveSound:LiveSound = new LiveSound( new egret.URLRequest(_sOutPath), null , _fLoadComplete ,_fLoadError );
				this.m_DictionaySound[_sOutPath] = _liveSound;
			}
		}
	
		
		/**
		 * 取得音效路徑
		 */
		protected getSoundPath( _string:string , _sFolderName:string ):string{
			var oData:Object = manager.SharedObjectManager.data;
			if(_sFolderName != null){
				if( oData[_sFolderName] && oData[_sFolderName][_string] ){
					return oData[_sFolderName][_string];
				}
			}
			
			if(this.m_DictionarySoundPath[_string]){
				if( _sFolderName != null ){
					if( !oData[_sFolderName] ){
						oData[_sFolderName] = {};
					}
					oData[_sFolderName][_string] = this.m_DictionarySoundPath[_string];		//在本地存路徑
				}

				return this.m_DictionarySoundPath[_string];
			}
			
			console.log("没有对应项-",_string);
			return _string;
		}	
		
